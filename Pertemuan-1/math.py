x=10
y=3

# tambah
z1 = x + y
print('Hasil Pertambahan adalah {} + {} = {}'.format(x,y,z1))

#kurang
z2 = x - y
print('Hasil Pengurangan adalah {} - {} = {}'.format(x,y,z2))

#kali
z3 = x * y
print('Hasil Perkalian adalah {} * {} = {}'.format(x,y,z3))

#bagi
z4 = x / y
print('Hasil Pembagian adalah {} / {} = {}'.format(x,y,z4))

#mod
z5 = x % y
print('Hasil Mod adalah {} % {} = {}'.format(x,y,z5))

#pangkat
z6 = x ** y
print('Hasil Pangkat adalah {} ** {} = {}'.format(x,y,z6))



